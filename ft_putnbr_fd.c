#include "libft.h"

static void	rec(int n, int fd)
{
	if (n == 0)
		return ;
	rec((n / 10), fd);
	write(fd, &"0123456789"[n % 10], 1);
}

void		ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		write(fd, "-", 1);
		rec((-(n / 10)), fd);
		write(fd, &"0123456789"[-(n % 10)], 1);
	}
	else
	{
		rec((n / 10), fd);
		write(fd, &"0123456789"[n % 10], 1);
	}
}
