#include "libft.h"

void	*ft_memcpy(void *s1, const void *s2, size_t n)
{
	char		*r1;
	const char	*r2;

	if (!s1 && !s2)
		return (0);
	r1 = s1;
	r2 = s2;
	while (n)
	{
		*r1++ = *r2++;
		--n;
	}
	return (s1);
}
