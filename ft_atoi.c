#include "libft.h"

int	ft_atoi(const char *str)
{
	int	val;
	int	sign;

	val = 0;
	sign = 1;
	while ((9 <= *str && *str <= 13) || *str == ' ')
		++str;
	if (*str == '+' || *str == '-')
	{
		if (*(str++) == '-')
			sign *= -1;
	}
	while ('0' <= *str && *str <= '9')
	{
		val *= 10;
		val += (*(str++) - '0');
	}
	return (val * sign);
}
